// Helper function to get products by category
export const getProductsByCategory = (category: string): ProductData[] => {
  if (category === 'all') return productsWithDetails;
  return productsWithDetails.filter(p => p.category === category);
};

// Helper function to get random products
export const getRandomProducts = (count: number): ProductData[] => {
  const shuffled = [...productsWithDetails].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
